static void hex(const char* tag, const uint8_t* b, size_t n) {
    printf("%s: ", tag);
    for (size_t i = 0; i < n; i++) printf("%02x", b[i]);
    printf("\n");
}

int main(void) {
    uint8_t pk[32], sk[32];
    speer_ed25519_keypair(pk, sk, seed1);
    hex("expect pk", pk1, 32);
    hex("got    pk", pk, 32);
    int pk_match = memcmp(pk, pk1, 32) == 0;
    printf("pk match: %s\n", pk_match ? "YES" : "NO");

    uint8_t sig[64];
    speer_ed25519_sign(sig, NULL, 0, pk1, seed1);
    hex("expect sig", sig1, 64);
    hex("got    sig", sig, 64);
    int sig_match = memcmp(sig, sig1, 64) == 0;
    printf("sig match: %s\n", sig_match ? "YES" : "NO");

    int ver = speer_ed25519_verify(sig1, NULL, 0, pk1);
    printf("verify with KAT sig: %s\n", ver == 0 ? "OK" : "FAIL");

    int ver2 = speer_ed25519_verify(sig, NULL, 0, pk1);
    printf("verify with our sig: %s\n", ver2 == 0 ? "OK" : "FAIL");

    return 0;
}
