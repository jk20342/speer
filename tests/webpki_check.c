int main(void) {
    speer_x509_t cert;
    if (speer_x509_parse(&cert, kSelfSignedDer, sizeof(kSelfSignedDer)) != 0) {
        printf("x509 parse: FAIL\n"); return 1;
    }
    printf("issuer dn len: %zu\n", cert.issuer_dn_len);
    printf("subject dn len: %zu\n", cert.subject_dn_len);
    printf("spki len: %zu\n", cert.spki_len);
    printf("not before: %lld\n", (long long)cert.not_before_utc);
    printf("not after:  %lld\n", (long long)cert.not_after_utc);
    printf("num san dns: %zu\n", cert.num_san_dns);
    if (cert.num_san_dns > 0) printf("san[0]: %s\n", cert.san_dns[0]);
    printf("is_ca: %d\n", cert.is_ca);
    printf("eku server auth: %d\n", (cert.ext_key_usage & X509_EKU_SERVER_AUTH) != 0);

    int match_ok = speer_x509_match_hostname(&cert, "example.com") == 0;
    int match_no = speer_x509_match_hostname(&cert, "evil.com") != 0;
    printf("hostname match example.com: %s\n", match_ok ? "ok" : "FAIL");
    printf("hostname reject evil.com:   %s\n", match_no ? "ok" : "FAIL");

    if (!match_ok || !match_no) { printf("webpki: FAIL\n"); return 1; }
    if (cert.spki_len == 0) { printf("webpki: FAIL (no spki)\n"); return 1; }
    if (cert.num_san_dns < 2) { printf("webpki: FAIL (san parse)\n"); return 1; }
    printf("webpki: ok\n");
    return 0;
}
